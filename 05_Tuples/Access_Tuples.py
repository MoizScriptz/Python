tuples = ("mango", "banana", "apple")
print(tuples)
print(type(tuples))

# Accessing elements in a tuple
print(tuples[0])  # Accessing the first element
print(tuples[1])  # Accessing the second element
print(tuples[2])  # Accessing the third element


# Accessing elements using negative indexing
print(tuples[-1])  # Accessing the last element


# Accessing elements using slicing
print(tuples[0:2])  # Accessing the first two elements


# Accessing elements using negative indexing with slicing
print(tuples[-2:])  # Accessing the last two elements


# Accessing elements using slicing with negative indexing
print(tuples[:-1])  # Accessing all elements except the last one
