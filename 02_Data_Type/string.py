a = "string"
b = 'string'
c = """This is a string
that spans multiple lines."""
d = '''This is also a string 
that spans multiple lines.'''
e = str(123)  # Convert integer to string
f = str(45.67)  # Convert float to string
print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
# String Concatenation
g = a + " " + b  # Concatenating two strings
print(g)
# String Repetition
h = a * 3  # Repeating the string three times
print(h)
# String Length
i = len(a)  # Getting the length of the string
print(i)
# String Indexing
j = a[0]  # Accessing the first character of the string
print(j)
